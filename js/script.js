function showHidePopup() {
	document.querySelector("#popup").classList.toggle('invisible');
}

function showHidePopupSuccess() {
	document.querySelector("#popup-success").classList.toggle('invisible');
}

function hidePopup() {
	document.querySelector("#popup").classList.add('invisible');
}

function hidePopupSuccess() {
	document.querySelector("#popup-success").classList.add('invisible');
}